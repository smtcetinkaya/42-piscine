#include <unistd.h>

void	ft_print_comb(void)
{
	char	k;
	char	l;
	char	m;

	k = '0';
	while ('7' >= k)
	{
		l = k + 1;
		while ('8' >= l)
		{
			m = l + 1;
			while ('9' >= m)
			{
				write(1, &k, 1);
				write(1, &l, 1);
				write(1, &m, 1);
				if (k != '7')
					write(1, ", ", 2);
				m++;
			}
			l++;
		}
		k++;
	}
}
