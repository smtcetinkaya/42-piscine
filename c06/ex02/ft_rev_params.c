#include <unistd.h>

int	main(int ag, char **argv)
{
	int		i;
	int		j;

	i = ag - 1;
	if (ag > 0)
	{
		while (i > 0)
		{
			while (argv[i][j])
			{
				write(1, &argv[i][j], 1);
				j++;
			}
			j = 0;
			write(1, &"\n", 1);
			i--;
		}
	}
}
